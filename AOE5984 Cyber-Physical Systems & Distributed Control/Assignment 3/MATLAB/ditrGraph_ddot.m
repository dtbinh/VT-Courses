function [xdot_dt]  = ditrGraph_ddot (t, x)

% % Star Formation
% D2=[0;50];
% D3=[-47.55;15.45];
% D4=[-29.4;-40.45];
% D5=[29.4;-40.4];
% D6=[47.55;15.45];

% % Polygon Formation
% D2=[40;60];
% D3=[40;-60];
% D4=[80;-60];
% D5=[80;60];
% D6=[120;0];

% Bracket Formation
D2=[0;80];
D3=[120;80];
D4=[250;0];
D5=[120;200];
D6=[250;200];

% Parameters
gamma = 10;
I2 = eye(2);
cKd = gamma*I2;
g = 1*I2;

% Control Inputs
u(1:2) = zeros(1,2);
xdot_dt(1:2) = x(3:4);
xdot_dt(3:4) = u(1:2);

u(3:4) = I2*((x(21:22)-x(5:6)+D2-D6)+g*(x(1:2)-x(5:6)+D2))+cKd*((x(23:24)-x(7:8))+g*(x(3:4)-x(7:8)));
xdot_dt(5:6) = x(7:8);
xdot_dt(7:8) = u(3:4);

u(5:6) = I2*((x(5:6)-x(9:10)+D3-D2)+g*(x(1:2)-x(9:10)+D3))+cKd*((x(7:8)-x(11:12))+g*(x(3:4)-x(11:12)));
xdot_dt(9:10) = x(11:12);
xdot_dt(11:12) = u(5:6);

u(7:8) = I2*(-x(13:14)+x(5:6)-D2+D4)+cKd*(x(7:8)-x(15:16));
xdot_dt(13:14) = x(15:16);
xdot_dt(15:16) = u(7:8);

u(9:10) = I2*(-x(17:18)+x(9:10)-D3+D5)+cKd*(x(11:12)-x(19:20));
xdot_dt(17:18) = x(19:20);
xdot_dt(19:20) = u(9:10);

u(11:12) = I2*(-2*x(21:22)+x(13:14)+x(17:18)-D4-D5+2*D6)+cKd*(x(15:16)+x(19:20)-2*x(23:24));
xdot_dt(21:22) = x(23:24);
xdot_dt(23:24) = u(11:12);

xdot_dt=xdot_dt'; % vdot transpose
